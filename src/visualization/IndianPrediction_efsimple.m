clear; clc; close all;

%% ==========================================================
% 1) Observed data (WHO 2010–2023)
%% ==========================================================
yearsObs = 2010:2023;

meanInc = [276 268 258 249 243 237 225 217 208 202 195 200 199 195]';
lowInc  = [114 138 141 141 171 181 191 193 185 180 172 173 170 164]';
uppInc  = [508 440 410 395 329 300 263 242 233 227 220 228 231 228]';

%% ==========================================================
% 2) Historical fitted trajectories (2010–2023)
%% ==========================================================

simple_fit_mean = [309.723 295.251 281.559 268.629 256.435 244.946 ...
                   234.130 223.952 214.375 205.365 196.884 188.901 ...
                   181.385 174.309]';

erlang_fit_mean = [287.020 276.670 266.618 256.888 247.499 238.460 ...
                   229.776 221.446 213.466 205.828 198.522 191.536 ...
                   184.859 178.483]';

efs_fit_mean = [295.459 283.943 272.865 262.243 252.083 242.384 ...
                233.140 224.339 215.968 208.008 200.443 193.251 ...
                186.418 179.927]';

%% ==========================================================
% 3) Forecast data (2024–2033)
%% ==========================================================
yearsF = 2024:2033;

simple_base_mean = [167.650 161.387 155.502 149.973 144.784 139.915 135.349 131.071 127.064 123.315]';
erlang_base_mean = [172.398 166.596 161.071 155.812 150.812 146.059 141.545 137.259 133.192 129.335]';
efs_base_mean    = [173.766 167.925 162.391 157.154 152.202 147.522 143.104 138.936 135.007 131.306]';

%% ==========================================================
% 4) Colors
%% ==========================================================
simpleColor = [0.00 0.35 0.90];
erlangColor = [0.90 0.10 0.10];
efsColor    = [0.55 0.00 0.85];

obsBandColor = [0.78 0.86 0.62];
obsMeanColor = [0.20 0.50 0.10];

%% ==========================================================
% 5) Plot
%% ==========================================================
figure('Position',[120 120 1350 720]);
hold on; grid on; box on;

% --- Observed UI ---
h_obs_ui = fill([yearsObs fliplr(yearsObs)], ...
                [lowInc' fliplr(uppInc')], ...
                obsBandColor, ...
                'EdgeColor','none', ...
                'FaceAlpha',0.45, ...
                'DisplayName','Observed 95% UI');

% --- Observed mean ---
h_obs_mean = plot(yearsObs, meanInc, 'o-', ...
                  'Color', obsMeanColor, ...
                  'LineWidth', 3, ...
                  'MarkerFaceColor', obsMeanColor, ...
                  'MarkerSize', 8, ...
                  'DisplayName', 'Observed mean');

% --- Historical model fits ---
h_simple_fit = plot(yearsObs, simple_fit_mean, '-', ...
                    'Color', simpleColor, ...
                    'LineWidth', 3, ...
                    'DisplayName','Simple fit');

h_erlang_fit = plot(yearsObs, erlang_fit_mean, '-', ...
                    'Color', erlangColor, ...
                    'LineWidth', 3, ...
                    'DisplayName','Gamma Distributed fit');

h_efs_fit = plot(yearsObs, efs_fit_mean, '-', ...
                 'Color', efsColor, ...
                 'LineWidth', 3, ...
                 'DisplayName','Gamma simple-posterior fit');

% --- Baseline ---
h_simple_base = plot(yearsF, simple_base_mean, '-', ...
                     'Color', simpleColor, ...
                     'LineWidth', 3, ...
                     'DisplayName','Simple baseline');

h_erlang_base = plot(yearsF, erlang_base_mean, '-', ...
                     'Color', erlangColor, ...
                     'LineWidth', 3, ...
                     'DisplayName','Gamma Distributed baseline');

h_efs_base = plot(yearsF, efs_base_mean, '-', ...
                  'Color', efsColor, ...
                  'LineWidth', 3, ...
                  'DisplayName','Gamma simple-posterior baseline');

% --- Smooth connectors ---
plot([2023 2024], [simple_fit_mean(end) simple_base_mean(1)], '-', ...
     'Color', simpleColor, 'LineWidth',2,'HandleVisibility','off');

plot([2023 2024], [erlang_fit_mean(end) erlang_base_mean(1)], '-', ...
     'Color', erlangColor, 'LineWidth',2,'HandleVisibility','off');

plot([2023 2024], [efs_fit_mean(end) efs_base_mean(1)], '-', ...
     'Color', efsColor, 'LineWidth',2,'HandleVisibility','off');

%% ==========================================================
% 6) Axis & Legend
%% ==========================================================
xlabel('Year', 'FontSize', 15);
ylabel('Incidence per 100,000', 'FontSize', 15);
title('TB annual incidence in India', 'FontSize', 17);

xlim([2010 2033]);

legend([h_obs_ui, h_obs_mean, ...
        h_simple_base, h_erlang_base, h_efs_base], ...
        'Location','northeast', ...
        'FontSize',11);

set(gca,'FontSize',13,'LineWidth',1.2);
